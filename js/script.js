function Filtra(lista, valor) {
    var value = valor.toUpperCase();
    var mostra = 0;
    $("#" + lista).children("h3").remove();
    $("#" + lista + " > li").each(function() {
        if ($(this).attr("data-search").search(value) > -1) {
            $(this).show();
            mostra = 1
        } else {
            $(this).hide();
        }
    });
    if (mostra == 0) {
        $("#" + lista).append('<h3 class="lista-vazia">Cliente "' + valor + '" não encontrado.</h3>');
    }
}